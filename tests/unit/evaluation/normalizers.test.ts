import { describe, it, expect } from 'vitest';
import {
  normalize,
  normalizeMarkdown,
  normalizeJson,
  normalizeYaml,
  normalizeText,
  NormalizationError,
} from '../../../src/evaluation/index.js';
import type { GeneratedFile } from '../../../src/types/index.js';

function mdFile(content: string, path = 'CLAUDE.md'): GeneratedFile {
  return { relativePath: path, content, description: '' };
}

describe('normalize', () => {
  it('detects markdown from .md extension', () => {
    const result = normalize(mdFile('# Title'));
    expect(result.type).toBe('markdown');
    expect(typeof result.payload).toBe('string');
  });

  it('detects JSON from .json extension and parses', () => {
    const result = normalize({
      relativePath: 'settings.json',
      content: '{"foo": 1}',
      description: '',
    });
    expect(result.type).toBe('json');
    expect(result.payload).toEqual({ foo: 1 });
  });

  it('detects YAML from .yaml extension and parses', () => {
    const result = normalize({
      relativePath: 'config.yaml',
      content: 'foo: 1',
      description: '',
    });
    expect(result.type).toBe('yaml');
    expect(result.payload).toEqual({ foo: 1 });
  });

  it('treats unknown extensions as text', () => {
    const result = normalize({
      relativePath: '.claude/hooks/dlp.py',
      content: 'print("hi")',
      description: '',
    });
    expect(result.type).toBe('text');
    expect(result.payload).toBe('print("hi")');
  });
});

describe('normalizeMarkdown', () => {
  it('strips a markdown stamp comment', () => {
    const input =
      '<!-- Generated by EmbedIQ v2.0.0 | schema:2 | 2026-04-19T12:34:56.000Z -->\n\n# Title\n\nBody';
    expect(normalizeMarkdown(input)).toBe('# Title\n\nBody');
  });

  it('strips a hash-style stamp on .yaml/.py/default files', () => {
    const input =
      '# Generated by EmbedIQ v2.0.0 | schema:2 | 2026-04-19T12:34:56Z\n\nkey: value';
    expect(normalizeMarkdown(input)).toBe('key: value');
  });

  it('collapses runs of 3+ blank lines to 2', () => {
    const input = 'A\n\n\n\n\nB';
    expect(normalizeMarkdown(input)).toBe('A\n\nB');
  });

  it('trims trailing whitespace per line', () => {
    const input = 'Foo   \nBar\t\n';
    expect(normalizeMarkdown(input)).toBe('Foo\nBar');
  });

  it('normalizes CRLF line endings to LF', () => {
    const input = 'A\r\nB\r\n';
    expect(normalize(mdFile(input)).payload).toBe('A\nB');
  });
});

describe('normalizeJson', () => {
  it('strips the top-level _embediq stamp key', () => {
    const input = JSON.stringify({
      permissions: { allow: ['Bash'] },
      _embediq: { version: '2.0.0', schema: 2, generatedAt: 'x' },
    });
    const parsed = normalizeJson(input, 'settings.json');
    expect(parsed).toEqual({ permissions: { allow: ['Bash'] } });
  });

  it('strips _embediq keys at nested levels too', () => {
    const input = JSON.stringify({
      outer: { _embediq: { t: 1 }, keep: 'this' },
    });
    const parsed = normalizeJson(input, 'x.json');
    expect(parsed).toEqual({ outer: { keep: 'this' } });
  });

  it('throws NormalizationError on malformed JSON', () => {
    expect(() => normalizeJson('{ not valid', 'bad.json')).toThrow(NormalizationError);
  });

  it('handles top-level primitives by wrapping in { value }', () => {
    const parsed = normalizeJson('42', 'scalar.json');
    expect(parsed).toEqual({ value: 42 });
  });
});

describe('normalizeYaml', () => {
  it('strips hash-style stamp and parses', () => {
    const input =
      '# Generated by EmbedIQ v2.0.0 | schema:2 | 2026-04-19T12:00:00Z\n\nname: alice\nage: 30';
    expect(normalizeYaml(input)).toEqual({ name: 'alice', age: 30 });
  });

  it('returns {} for empty content', () => {
    expect(normalizeYaml('')).toEqual({});
  });
});

describe('normalizeText', () => {
  it('follows the same rules as markdown (stamp + whitespace)', () => {
    const input = '# Generated by EmbedIQ v2.0.0 | schema:2 | 2026-04-19T00:00:00Z\n\n#!/usr/bin/env python\nprint("x")   \n';
    expect(normalizeText(input)).toBe('#!/usr/bin/env python\nprint("x")');
  });
});
