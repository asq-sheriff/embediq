import { describe, it, expect } from 'vitest';
import {
  DEFAULT_WEIGHTS,
  Scorer,
  type Weights,
} from '../../../src/evaluation/index.js';
import type { GeneratedFile } from '../../../src/types/index.js';
import { Dimension } from '../../../src/types/index.js';

function file(relativePath: string, content: string): GeneratedFile {
  return { relativePath, content, description: '' };
}

function mdBlock(title: string): string {
  return `# ${title}\n\nBody text for ${title}`;
}

describe('Scorer', () => {
  const scorer = new Scorer();

  describe('empty inputs', () => {
    it('returns overallScore 1.0 when both sides are empty', () => {
      const result = scorer.score({
        generated: [],
        expected: [],
        weights: DEFAULT_WEIGHTS,
      });
      expect(result.overallScore).toBe(1);
      expect(result.checks).toHaveLength(0);
      expect(result.fileScores).toHaveLength(0);
    });
  });

  describe('identical file sets', () => {
    it('scores identical content at 1.0', () => {
      const files = [
        file('CLAUDE.md', mdBlock('Title')),
        file('.claude/settings.json', JSON.stringify({ model: 'sonnet' })),
      ];
      const result = scorer.score({
        generated: files,
        expected: files,
        weights: DEFAULT_WEIGHTS,
      });
      expect(result.overallScore).toBe(1);
      for (const fs of result.fileScores) expect(fs.score).toBe(1);
    });

    it('ignores the generation-header stamp when scoring', () => {
      const expected = file('CLAUDE.md', '# Title\n\nBody');
      const stamped = file(
        'CLAUDE.md',
        '<!-- Generated by EmbedIQ v2.0.0 | schema:2 | 2026-04-19T12:00:00Z -->\n\n# Title\n\nBody',
      );
      const result = scorer.score({
        generated: [stamped],
        expected: [expected],
        weights: DEFAULT_WEIGHTS,
      });
      expect(result.overallScore).toBe(1);
    });
  });

  describe('missing files', () => {
    it('emits a critical structural check with score = missingFilePenalty (default 0)', () => {
      const result = scorer.score({
        generated: [],
        expected: [file('CLAUDE.md', '# T')],
        weights: DEFAULT_WEIGHTS,
      });
      expect(result.fileScores).toHaveLength(1);
      const fs = result.fileScores[0];
      expect(fs.status).toBe('missing');
      expect(fs.score).toBe(0);
      expect(fs.checks[0].severity).toBe('critical');
      expect(fs.checks[0].category).toBe('structural');
      expect(result.overallScore).toBe(0);
    });

    it('uses missingFilePenalty when raised above 0 (partial credit)', () => {
      const weights: Weights = { ...DEFAULT_WEIGHTS, missingFilePenalty: 0.25 };
      const result = scorer.score({
        generated: [],
        expected: [file('CLAUDE.md', '# T')],
        weights,
      });
      expect(result.overallScore).toBe(0.25);
    });

    it('drags down the aggregate when one file is missing alongside a matched file', () => {
      const expected = [
        file('CLAUDE.md', mdBlock('Keep')),
        file('.claude/rules/hipaa.md', mdBlock('Rules')),
      ];
      const generated = [file('CLAUDE.md', mdBlock('Keep'))];
      const result = scorer.score({
        generated,
        expected,
        weights: DEFAULT_WEIGHTS,
      });
      expect(result.overallScore).toBeGreaterThan(0);
      expect(result.overallScore).toBeLessThan(1);
      const missing = result.fileScores.find((f) => f.status === 'missing');
      expect(missing?.filePath).toBe('.claude/rules/hipaa.md');
    });
  });

  describe('extra files', () => {
    it('does not change overallScore at default (extraFilePenalty = 0)', () => {
      const result = scorer.score({
        generated: [
          file('CLAUDE.md', mdBlock('Title')),
          file('.claude/extras.md', mdBlock('Extra')),
        ],
        expected: [file('CLAUDE.md', mdBlock('Title'))],
        weights: DEFAULT_WEIGHTS,
      });
      expect(result.overallScore).toBe(1);
      const extra = result.fileScores.find((f) => f.status === 'extra');
      expect(extra).toBeDefined();
      expect(extra!.weightTotal).toBe(0);
    });

    it('drops overallScore when extraFilePenalty is raised', () => {
      const weights: Weights = { ...DEFAULT_WEIGHTS, extraFilePenalty: 0.5 };
      const result = scorer.score({
        generated: [
          file('CLAUDE.md', mdBlock('Title')),
          file('.claude/extras.md', mdBlock('Extra')),
        ],
        expected: [file('CLAUDE.md', mdBlock('Title'))],
        weights,
      });
      expect(result.overallScore).toBeLessThan(1);
      const extra = result.fileScores.find((f) => f.status === 'extra');
      expect(extra!.weightTotal).toBeGreaterThan(0);
      expect(extra!.score).toBe(0.5);
    });
  });

  describe('unparseable files', () => {
    it('emits a critical unparseable check when expected JSON is malformed', () => {
      const result = scorer.score({
        generated: [file('.claude/settings.json', '{"valid": true}')],
        expected: [file('.claude/settings.json', '{ not valid')],
        weights: DEFAULT_WEIGHTS,
      });
      const fs = result.fileScores[0];
      expect(fs.status).toBe('matched');
      expect(fs.score).toBe(0);
      expect(fs.checks[0].id).toContain('structural.unparseable');
      expect(fs.checks[0].severity).toBe('critical');
    });

    it('emits a critical unparseable check when actual JSON is malformed', () => {
      const result = scorer.score({
        generated: [file('.claude/settings.json', '{ broken')],
        expected: [file('.claude/settings.json', '{"valid": true}')],
        weights: DEFAULT_WEIGHTS,
      });
      const fs = result.fileScores[0];
      expect(fs.score).toBe(0);
      expect(fs.checks[0].id).toContain('structural.unparseable');
    });
  });

  describe('file-level score aggregation', () => {
    it('computes file score as the weighted mean of its own checks', () => {
      const expected = file('CLAUDE.md', '# A\n\n## B\n\n## C');
      const actual = file('CLAUDE.md', '# A\n\n## B'); // missing one section
      const result = scorer.score({
        generated: [actual],
        expected: [expected],
        weights: DEFAULT_WEIGHTS,
      });
      const fs = result.fileScores[0];
      expect(fs.score).toBeGreaterThan(0);
      expect(fs.score).toBeLessThan(1);
    });
  });

  describe('dimension aggregation', () => {
    it('buckets check weights by dimension', () => {
      const result = scorer.score({
        generated: [
          file('CLAUDE.md', mdBlock('Title')),
          file('.claude/rules/hipaa-compliance.md', mdBlock('HIPAA')),
        ],
        expected: [
          file('CLAUDE.md', mdBlock('Title')),
          file('.claude/rules/hipaa-compliance.md', mdBlock('HIPAA')),
        ],
        weights: DEFAULT_WEIGHTS,
      });
      const strat = result.dimensionScores.find(
        (d) => d.dimension === Dimension.STRATEGIC_INTENT,
      );
      const reg = result.dimensionScores.find(
        (d) => d.dimension === Dimension.REGULATORY_COMPLIANCE,
      );
      expect(strat?.score).toBe(1);
      expect(reg?.score).toBe(1);
      expect(strat?.checkCount).toBeGreaterThan(0);
      expect(reg?.checkCount).toBeGreaterThan(0);
    });

    it('surfaces regressions under the correct dimension', () => {
      const expected = [
        file('CLAUDE.md', mdBlock('Title')),
        file('.claude/rules/hipaa-compliance.md', '# HIPAA\n\n## PHI Handling\n\n## BAA'),
      ];
      const generated = [
        file('CLAUDE.md', mdBlock('Title')),
        file('.claude/rules/hipaa-compliance.md', '# HIPAA\n\n## PHI Handling'), // missing BAA
      ];
      const result = scorer.score({
        generated,
        expected,
        weights: DEFAULT_WEIGHTS,
      });
      const strat = result.dimensionScores.find(
        (d) => d.dimension === Dimension.STRATEGIC_INTENT,
      );
      const reg = result.dimensionScores.find(
        (d) => d.dimension === Dimension.REGULATORY_COMPLIANCE,
      );
      expect(strat?.score).toBe(1);
      expect(reg!.score).toBeLessThan(1);
    });
  });

  describe('generator aggregation', () => {
    it('buckets files by generator name', () => {
      const result = scorer.score({
        generated: [
          file('CLAUDE.md', mdBlock('T')),
          file('.claude/settings.json', JSON.stringify({ model: 'sonnet' })),
          file('.claude/hooks/dlp-guard.py', 'print("ok")'),
        ],
        expected: [
          file('CLAUDE.md', mdBlock('T')),
          file('.claude/settings.json', JSON.stringify({ model: 'sonnet' })),
          file('.claude/hooks/dlp-guard.py', 'print("ok")'),
        ],
        weights: DEFAULT_WEIGHTS,
      });
      const names = result.generatorScores.map((g) => g.generatorName).sort();
      expect(names).toContain('CLAUDE.md');
      expect(names).toContain('settings.json');
      expect(names).toContain('hooks');
      for (const g of result.generatorScores) expect(g.score).toBe(1);
    });

    it('routes unknown files to the "uncategorized" bucket', () => {
      const result = scorer.score({
        generated: [file('random/file.md', '# X')],
        expected: [file('random/file.md', '# X')],
        weights: DEFAULT_WEIGHTS,
      });
      const uncategorized = result.generatorScores.find(
        (g) => g.generatorName === 'uncategorized',
      );
      expect(uncategorized).toBeDefined();
      expect(uncategorized!.score).toBe(1);
    });
  });

  describe('honors weight overrides', () => {
    it('amplifies the contribution of a per-file override', () => {
      const weights: Weights = {
        ...DEFAULT_WEIGHTS,
        byFile: { 'CLAUDE.md': 100 },
      };
      const expected = [
        file('CLAUDE.md', '# Title'),
        file('.claude/settings.json', JSON.stringify({ model: 'sonnet' })),
      ];
      const generated = [
        file('CLAUDE.md', '# Totally\n\n# Wrong'),
        file('.claude/settings.json', JSON.stringify({ model: 'sonnet' })),
      ];
      const boosted = new Scorer().score({ expected, generated, weights });
      const neutral = new Scorer().score({
        expected,
        generated,
        weights: DEFAULT_WEIGHTS,
      });
      // Same content, higher weight on the failing file → lower aggregate score.
      expect(boosted.overallScore).toBeLessThan(neutral.overallScore);
    });
  });
});
